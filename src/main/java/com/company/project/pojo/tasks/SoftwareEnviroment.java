package com.company.project.pojo.tasks;

public class SoftwareEnviroment {
    // 操作系统
    private String os;
    // 数据库
    private String database;
    // 软件开发环境
    private String devEnvir;

    public String getOs() {
        return os;
    }

    public void setOs(String os) {
        this.os = os;
    }

    public String getDatabase() {
        return database;
    }

    public void setDatabase(String database) {
        this.database = database;
    }

    public String getDevEnvir() {
        return devEnvir;
    }

    public void setDevEnvir(String devEnvir) {
        this.devEnvir = devEnvir;
    }
}
